function removeSuffix(word: string, suffixes: string[], minRemaining: number): string {
  for (const suffix of suffixes) {
    if (word.endsWith(suffix) && word.length - suffix.length >= minRemaining) {
      return word.slice(0, word.length - suffix.length)
    }
  }
  return word
}

function stem(word: string): string {
  if (word.length < 4) return word

  let result = removeSuffix(word, NOUN_SUFFIXES, 3)
  if (result !== word) return result

  result = removeSuffix(word, VERB_SUFFIXES, 3)
  if (result !== word) return result

  result = removeSuffix(word, ADJECTIVE_SUFFIXES, 3)
  return result
}

