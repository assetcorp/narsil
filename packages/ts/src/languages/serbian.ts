function stem(word: string): string {
  if (word.length < 4) return word

  for (const [suffix, minLen] of SUFFIXES) {
    if (word.endsWith(suffix) && word.length - suffix.length >= minLen) {
      return word.slice(0, word.length - suffix.length)
    }
  }

  return word
}

